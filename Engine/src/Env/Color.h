	template<class T>
	constexpr T HexToVec4(Color color, float alpha = 1.0f) {
		uint32_t hexValue = static_cast<uint32_t>(color);
		float red = static_cast<float>((hexValue >> 16) & 0xFF) / 255.0f;
		float green = static_cast<float>((hexValue >> 8) & 0xFF) / 255.0f;
		float blue = static_cast<float>(hexValue & 0xFF) / 255.0f;
		return T(red, green, blue, alpha);
	}

